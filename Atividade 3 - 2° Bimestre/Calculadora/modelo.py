import math

class Calculadora:

    # operacoes 
    def somar(self, v1, v2):
        return v1 + v2

    def subtrair(self, v1, v2):
        return v1 - v2

    def multiplicar(self, v1, v2):
        return v1 * v2

    def dividir(self, v1, v2):
        return v1 / v2

    def raizQuadrada(self, v1):
        return math.sqrt(v1)